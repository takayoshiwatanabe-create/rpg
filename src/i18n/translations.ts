export const getTranslation = (
  locale: Locale,
  key: string,
  params?: Record<string, string | number>,
): string => {
  let translation = translations[locale]?.[key] || translations[DEFAULT_LOCALE]?.[key] || key;

  if (params) {
    for (const paramKey in params) {
      translation = translation.replace(
        new RegExp(`{${paramKey}}`, "g"),
        String(params[paramKey]),
      );
    }
  }
  return translation;
};

let currentLocale: Locale = DEFAULT_LOCALE;

export const setLocale = (locale: Locale) => {
  currentLocale = locale;
};

export const getLocale = (): Locale => {
  if (currentLocale === DEFAULT_LOCALE) {
    const systemLocale = Localization.getLocales()[0]?.languageCode as Locale;
    if (systemLocale && translations[systemLocale]) {
      currentLocale = systemLocale;
    }
  }
  return currentLocale;
};

export const t = (key: string, params?: Record<string, string | number>): string => {
  return getTranslation(getLocale(), key, params);
};

export const getIsRTL = (): boolean => {
  const locale = getLocale();
  return locale === "ar"; // Arabic is the only RTL language currently supported
};

export const getLang = (): string => {
  return getLocale();
};

